
SELECT (CASE g.grade>=8 WHEN TRUE THEN s.name ELSE null END), g.grade, s.marks
FROM students s INNER JOIN grades g ON s.marks BETWEEN min_mark AND max_mark
ORDER BY g.grade DESC,s.name,s.marks;


SELECT (
    CASE  GRADES.GRADE >= 8 WHEN TRUE THEN STUDENTS.NAME ELSE NULL END    
    )   , GRADES.GRADE , STUDENTS.MARKS
FROM STUDENTS 
INNER JOIN GRADES ON
STUDENTS.MARKS BETWEEN GRADES.min_mark AND GRADES.max_mark
ORDER BY GRADES.grade DESC,STUDENTS.name,STUDENTS.marks;
